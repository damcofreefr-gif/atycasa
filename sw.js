/* Service worker — stratégie "réseau d'abord, cache en secours".
   Avantage : chaque push sur GitHub met l'app à jour dès la prochaine
   ouverture avec connexion, et l'app reste utilisable hors ligne. */

const CACHE = "maison-v10";
const ASSETS = [
  "./",
  "./index.html",
  "./app.js",
  "./atyclock.html",
  "./atyclock.js",
  "./manifest.webmanifest",
  "./icons/icon-192.png",
  "./icons/icon-512.png",
];

self.addEventListener("install", (e) => {
  e.waitUntil(caches.open(CACHE).then((c) => c.addAll(ASSETS)));
  self.skipWaiting();
});

self.addEventListener("activate", (e) => {
  e.waitUntil(
    caches.keys().then((keys) =>
      Promise.all(keys.filter((k) => k !== CACHE).map((k) => caches.delete(k)))
    )
  );
  self.clients.claim();
});

self.addEventListener("fetch", (e) => {
  if (e.request.method !== "GET") return;
  e.respondWith(
    fetch(e.request)
      .then((res) => {
        const copy = res.clone();
        caches.open(CACHE).then((c) => c.put(e.request, copy));
        return res;
      })
      .catch(() => caches.match(e.request))
  );
});
